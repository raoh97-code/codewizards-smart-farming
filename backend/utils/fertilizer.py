def get_fertilizer_plan(crop, soil_data, optional_nutrients):
    crop = crop.lower()

    if crop not in FERTILIZER_DB:
        return {"message": "No detailed fertilizer plan available"}

    plan = FERTILIZER_DB[crop]
    deficiencies = []

    # Macronutrient deficiencies
    if soil_data["N"] < 50:
        deficiencies.append("Nitrogen low → Increase Urea")

    if soil_data["P"] < 20:
        deficiencies.append("Phosphorus low → Add DAP")

    if soil_data["K"] < 20:
        deficiencies.append("Potassium low → Add MOP")

    # Micronutrient deficiencies
    for key, value in optional_nutrients.items():
        if value:
            try:
                if float(value) < 1:
                    deficiencies.append(f"{key} deficiency detected")
            except:
                pass

    return {
        "stages": plan["stages"],
        "micronutrients": plan["micronutrients"],
        "deficiencies": deficiencies
    }